const numInput = document.querySelector(".number-input")
const countBtn = document.querySelector(".count")

const radixInput = document.querySelector(".radix-input")
const radixOutput = document.querySelector(".radix-output")

const numOutput = document.querySelector(".number-output")
const copyBtn = document.querySelector("#copy-icon")

document.getElementById('erase-icon').onclick = function() {
    document.getElementById('number-input').value = '';
  }

copyBtn.addEventListener('click', copy)

function copy() {
    numOutput.select()
    document.execCommand("copy");
    document.getElementById('copied-message').style.opacity = "100";
    setTimeout(() => document.getElementById('copied-message').style.opacity = "0", 1500);
}

countBtn.addEventListener('click', () => {
    numOutput.value = changeBase(parseInt(numInput.value), parseInt(radixInput.value), parseInt(radixOutput.value))
})

function changeBase(number, fromBase, toBase) {
    if (fromBase == 10)
        return (parseInt(number)).toString(toBase)
    else if (toBase == 10)
        return parseInt(number, fromBase);
    else{
        var numberInDecimal = parseInt(number, fromBase);
        return (parseInt(numberInDecimal)).toString(toBase);
    }
}

